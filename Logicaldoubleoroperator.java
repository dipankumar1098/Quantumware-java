class Logicaldoubleoroperator{
	public static void main(String []args){
		int a=50;
		int b=60;
		int c=70;

		float d=5.5f;
		float e=7.7f;

		double cv=10.20;
		double mn=30.40;

		System.out.println(a<b||b<c);
		System.out.println(cv<mn||e<d);
		System.out.println(d>e||b<c);
		System.out.println(a>b||b>c);

		
	}
}